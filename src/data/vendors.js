// ===== Helpers =====

// Pandit's menu uses these English item names; key by the user's ceremony.
const PANDIT_CEREMONY_TO_MENU = {
  wedding: 'Wedding ceremony',
  annaprashan: 'Annaprashan / Upanayan',
  upanayan: 'Annaprashan / Upanayan',
  shradhh: 'Shradhh',
};

const HIDDEN_CATEGORIES_BY_CEREMONY = {
  anniversary: new Set(['PANDIT']),
  engagement:  new Set(['PANDIT']),
  shradhh:     new Set(['PHOTOGRAPHER', 'MUSIC', 'LIGHTS']),
};

export function filterVendorsByCeremony(vendors, ceremonyKey) {
  const hidden = HIDDEN_CATEGORIES_BY_CEREMONY[ceremonyKey];
  if (!hidden) return vendors;
  return vendors.filter(v => !hidden.has(v.category));
}

function formatINR(n) {
  return '₹' + Number(n).toLocaleString('en-IN');
}

/**
 * Returns the vendor's menu items already localised in `lang`.
 * For caterers, the menu is built from CATERER_REGIONAL[city] + NORTH_INDIAN_PAIR.
 * For everyone else, returns the vendor's static menu with Hindi-name swap when applicable.
 */
export function getVendorMenu(vendor, city, lang = 'en') {
  if (vendor.category === 'CATERER') {
    const regional = CATERER_REGIONAL[city] || CATERER_REGIONAL.Kolkata;
    return [
      { name: regional.veg[lang]      || regional.veg.en,      price: regional.veg.price },
      { name: regional.nonVeg[lang]   || regional.nonVeg.en,   price: regional.nonVeg.price },
      { name: NORTH_INDIAN_PAIR.veg[lang]    || NORTH_INDIAN_PAIR.veg.en,    price: NORTH_INDIAN_PAIR.veg.price },
      { name: NORTH_INDIAN_PAIR.nonVeg[lang] || NORTH_INDIAN_PAIR.nonVeg.en, price: NORTH_INDIAN_PAIR.nonVeg.price },
    ];
  }
  return (vendor.menu || []).map(m => ({
    name: lang === 'hi' && m.nameHi ? m.nameHi : m.name,
    price: m.price,
  }));
}

/** English-named menu used by lookups that need a stable key (e.g., Pandit price-by-ceremony). */
function englishMenu(vendor, city) {
  return getVendorMenu(vendor, city, 'en');
}

export function getVendorPriceLabel(vendor, ceremonyKey, t, city) {
  if (!vendor) return '';
  const menuEn = englishMenu(vendor, city);

  if (vendor.category === 'CATERER') {
    if (menuEn.length === 0) return '';
    const min = Math.min(...menuEn.map(m => m.price));
    const plate = t ? t('cat.perPlate') : 'Plate';
    const from = t ? t('price.from') : 'From';
    return `${from} ${formatINR(min)} / ${plate}`;
  }

  if (vendor.category === 'PANDIT') {
    const targetName = PANDIT_CEREMONY_TO_MENU[ceremonyKey];
    const item = menuEn.find(m => m.name === targetName) || menuEn[0];
    return formatINR(item.price);
  }

  if (menuEn.length === 0) return '';
  const min = Math.min(...menuEn.map(m => m.price));
  const from = t ? t('price.from') : 'From';
  return `${from} ${formatINR(min)}`;
}

export function getVendorMinPrice(vendor, city) {
  const menuEn = englishMenu(vendor, city);
  if (menuEn.length === 0) return 0;
  return Math.min(...menuEn.map(m => m.price));
}

export function getVendorMaxPrice(vendor, city) {
  const menuEn = englishMenu(vendor, city);
  if (menuEn.length === 0) return 0;
  return Math.max(...menuEn.map(m => m.price));
}

/** City-specific locality (and distance) for a vendor in the current language. */
export function getVendorLocation(vendor, city, lang = 'en') {
  const loc = vendor.locations?.[city];
  if (!loc) {
    const first = Object.values(vendor.locations || {})[0];
    return first ? { locality: first[lang] || first.en, distanceKm: first.distanceKm } : { locality: '', distanceKm: 0 };
  }
  return { locality: loc[lang] || loc.en, distanceKm: loc.distanceKm };
}

/** Full "Locality, City" string in the active language. */
export function getVendorLocationString(vendor, city, lang = 'en', t) {
  const { locality } = getVendorLocation(vendor, city, lang);
  const cityLabel = t ? t(`city.${city}`) : city;
  return locality ? `${locality}, ${cityLabel}` : cityLabel;
}

/** Vendor display name in the active language. */
export function getVendorName(vendor, lang = 'en') {
  return lang === 'hi' && vendor.nameHi ? vendor.nameHi : vendor.name;
}
